import Foundation
import AppKit

actor AppIdentifierService {
    
    // MARK: - Cached Results
    
    private var cache: [String: CachedResult] = [:]
    
    struct CachedResult {
        let appName: String?
        let bundleID: String?
        let timestamp: Date
        let confidence: Confidence
        
        enum Confidence {
            case high
            case medium
            case low
        }
        
        var isExpired: Bool {
            Date().timeIntervalSince(timestamp) > 7 * 24 * 60 * 60
        }
    }
    
    // MARK: - Public API
    
    func identifyApp(
        itemName: String,
        bundleIDHint: String? = nil
    ) async -> (appName: String?, bundleID: String?, confidence: CachedResult.Confidence) {
        
        let cleanName = cleanItemName(itemName)
        
        if let cached = cache[cleanName], !cached.isExpired {
            return (cached.appName, cached.bundleID, cached.confidence)
        }
        
        if let bundleIDHint = bundleIDHint {
            if let result = await searchByBundleID(bundleIDHint) {
                cacheResult(for: cleanName, result: result)
                return (result.appName, result.bundleID, result.confidence)
            }
        }
        
        if let result = await searchByName(cleanName) {
            cacheResult(for: cleanName, result: result)
            return (result.appName, result.bundleID, result.confidence)
        }
        
        let emptyResult = CachedResult(
            appName: nil,
            bundleID: nil,
            timestamp: Date(),
            confidence: .low
        )
        cacheResult(for: cleanName, result: emptyResult)
        return (nil, nil, .low)
    }
    
    // MARK: - Search Methods
    
    private func searchByBundleID(_ bundleID: String) async -> CachedResult? {
        if let localResult = searchLocalLaunchServices(bundleID: bundleID) {
            return localResult
        }
        
        let bundleComponents = bundleID.components(separatedBy: ".")
        
        let cleanComponents = bundleComponents.filter { component in
            !["com", "org", "net", "io", "app", "de", "fr", "uk", "us", "co"].contains(component.lowercased())
        }
        
        for component in cleanComponents {
            if let heuristicResult = applyHeuristics(component) {
                return heuristicResult
            }
        }
        
        if let lastComponent = cleanComponents.last, lastComponent.count >= 3 {
            return CachedResult(
                appName: lastComponent.capitalized,
                bundleID: bundleID,
                timestamp: Date(),
                confidence: .low
            )
        }
        
        return nil
    }
    
    private func searchByName(_ name: String) async -> CachedResult? {
        if let heuristicResult = applyHeuristics(name) {
            return heuristicResult
        }
        
        if let knownPattern = matchKnownPatterns(name) {
            return knownPattern
        }
        
        return nil
    }
    
    // MARK: - Local Search
    
    private func searchLocalLaunchServices(bundleID: String) -> CachedResult? {
        let workspace = NSWorkspace.shared
        
        if let appURL = workspace.urlForApplication(withBundleIdentifier: bundleID) {
            let appName = appURL.deletingPathExtension().lastPathComponent
            return CachedResult(
                appName: appName,
                bundleID: bundleID,
                timestamp: Date(),
                confidence: .high
            )
        }
        
        return nil
    }
    
