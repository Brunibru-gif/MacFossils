    private func matchKnownPatterns(_ name: String) -> CachedResult? {
        let nameLower = name.lowercased()
        
        if nameLower.hasSuffix("helper") || nameLower.hasSuffix("agent") {
            let baseName = nameLower
                .replacingOccurrences(of: "helper", with: "")
                .replacingOccurrences(of: "agent", with: "")
                .trimmingCharacters(in: .whitespaces)
            
            if !baseName.isEmpty {
                return CachedResult(
                    appName: baseName.capitalized,
                    bundleID: nil,
                    timestamp: Date(),
                    confidence: .low
                )
            }
        }
        
        return nil
    }
    
    // MARK: - Helpers
    
    private func cleanItemName(_ name: String) -> String {
        var cleaned = name
            .lowercased()
            .replacingOccurrences(of: ".plist", with: "")
            .replacingOccurrences(of: ".app", with: "")
            .replacingOccurrences(of: ".framework", with: "")
        
        let prefixes = ["com.", "org.", "net.", "io.", "app."]
        for prefix in prefixes {
            if cleaned.hasPrefix(prefix) {
                cleaned = String(cleaned.dropFirst(prefix.count))
            }
        }
        
        return cleaned
    }
    
